public class Carro {
       
	private String modelo;
   	private String cor;
       
   	public String getmodelo() {
            return this.modelo;
    }
    
    public String setmodelo(String modelo) {
            return this.modelo = modelo;
    }
    
    public String getcor() {
            return this.cor;
    }
    
    public String setcor(String cor) {
            return this.cor = cor;
    }
   
    public String escolhaModelo() {
            return modelo;
    }
    
    public String escolhaCor() {
            return cor;
    }
}